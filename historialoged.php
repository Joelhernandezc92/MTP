            <h2>Historia</h2>
            <p>Durante los años de existencia, el colegio Montepiedra ha enfrentado diversos desafíos significativos. En primer lugar, se han experimentado dificultades económicas casi anualmente, con la excepción del año 2020 debido a la pandemia. El colegio ha operado con números rojos, salvo durante el confinamiento. Además, ha tenido que lidiar con las demandas del Ministerio de Educación, que en varias ocasiones intentó comprar o cerrar la institución. Sin embargo, mediante conversaciones, aclaraciones y presentación de documentación, el colegio ha superado estos desafíos.</p>
            <p>Destacamos a dos exalumnos ejemplares que han dejado su huella en diferentes campos. El ingeniero Víctor Rivera, que inició en Montepiedra como ayudante de cátedra y ahora desempeña un papel crucial como profesor técnico y director del Centro de Capacitación Laboral. También, Víctor Rizo, quien tras graduarse, recibió una beca para estudios en tecnología de maquinaria pesada y hoy trabaja en una empresa multinacional.</p>
        <p>La propuesta educativa de Montepiedra ha evolucionado con el tiempo. A pesar de las actualizaciones constantes en la malla curricular por parte del Ministerio de Educación, Montepiedra va más allá, ofreciendo módulos adicionales en áreas como refrigeración y neumática para brindar una formación más completa y adaptada a las demandas laborales.</p>
        <p>El nombre "Montepiedra" tiene un origen fascinante. Durante la construcción del colegio, se descubrió que el monte ocultaba una roca azul, una de las más duras del planeta. La dificultad en nivelar el terreno llevó a la sugerencia del sacerdote: "Wow, este es un monte de piedra", originando así el nombre de la institución.
        Eventos significativos, como el terremoto de 2016, han marcado a la comunidad escolar. Aunque no ocurrió dentro del colegio, el impacto fue profundo. La pérdida de un estudiante y 11 miembros de su familia generó un impacto duradero. También se recuerda la trágica muerte de un estudiante por cáncer, cuyo deseo de ver a sus padres casados se cumplió gracias al apoyo de Montepiedra.
        </p>
        <p>Aunque hemos enfrentado fuertes desafíos, siempre buscamos resolverlos mediante conversaciones y acuerdos. Existe un ambiente de armonía y camaradería entre los compañeros y el personal de Montepiedra, lo cual valoramos enormemente. Nos sentimos orgullosos de pertenecer a esta comunidad.</p>
